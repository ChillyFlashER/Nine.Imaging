namespace Nine.Imaging
{
    /// <summary>
    /// Defines how a image should be flipped.
    /// </summary>
    public enum FlippingType
    {
        /// <summary>
        /// Dont flip the image.
        /// </summary>
        None,
        /// <summary>
        /// Flip the image horizontally.
        /// </summary>
        Horizontal,
        /// <summary>
        /// Flip the image vertically.
        /// </summary>
        Vertical,
    }
}
